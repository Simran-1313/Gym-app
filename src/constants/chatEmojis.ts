export function getRecentEmojis(): string[] {
  try {
    const raw = (globalThis as Record<string, unknown>).__recentEmojis as string[] | undefined;
    return raw ?? [];
  } catch { return []; }
}

export function saveRecentEmoji(emoji: string): string[] {
  try {
    const store = (globalThis as Record<string, unknown>);
    const prev: string[] = (store.__recentEmojis as string[]) ?? [];
    const next = [emoji, ...prev.filter((e) => e !== emoji)].slice(0, 24);
    store.__recentEmojis = next;
    return next;
  } catch { return [emoji]; }
}
